class TestEBayesMixedInfdfRParity:
    """e_bayes lods branch when some df.prior = Inf and others finite.

    Exercises ebayes.R:79-86: mixed branch applying the chi-squared limit
    kernel to Inf-df genes and the standard kernel to finite-df genes.
    Uses a synthetic fit dict to force the mix; compares to R's closed-form
    reference values computed in the fixture generator.
    """

    def test_mixed_infdf_lods(self):
        t_mat = load_r_csv_no_index("ebayes_mixed_infdf_t").values
        stdev = load_r_csv_no_index("ebayes_mixed_infdf_stdev").values
        df = load_r_csv_no_index("ebayes_mixed_infdf_df")
        scalars = load_r_csv_no_index("ebayes_mixed_infdf_scalars")
        r_lods = load_r_csv_no_index("ebayes_mixed_infdf_lods").values

        df_prior = df["df_prior"].values
        df_residual = df["df_residual"].values.astype(float)
        df_total = df["df_total"].values
        var_prior = float(scalars["var_prior"].iloc[0])
        proportion = float(scalars["proportion"].iloc[0])

        # Construct a fit dict with: t, stdev_unscaled, sigma, df_residual,
        # and the synthetic df_prior, then compute lods using e_bayes's
        # published formula (replicated here rather than going through
        # e_bayes because we need to inject df_prior directly).
        n = t_mat.shape[0]
        t2 = t_mat ** 2
        r_val = (stdev ** 2 + var_prior) / (stdev ** 2)

