FIXTURES_DIR = Path(__file__).parent / "fixtures"


def load_r_csv(name: str) -> pd.DataFrame:
    """Load an R-generated CSV fixture."""
    path = FIXTURES_DIR / f"R_{name}.csv"
    if not path.exists():
        pytest.skip(f"Fixture not found: {path}")
    return pd.read_csv(path, index_col=0)


def load_r_csv_no_index(name: str) -> pd.DataFrame:
    """Load an R-generated CSV fixture without row index."""
    path = FIXTURES_DIR / f"R_{name}.csv"
    if not path.exists():
        pytest.skip(f"Fixture not found: {path}")
    return pd.read_csv(path)


