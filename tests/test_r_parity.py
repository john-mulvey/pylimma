    def test_rank_deficient_beta_nan_fill(self):
        # R: beta[is.na(beta)] <- 0 when lmFit drops a redundant column.
        # With batch perfectly confounded with group, the batch coefficient
        # becomes NA in R; output == input.
        # Python's lm_fit may pivot the redundant column differently (dropping
        # a preserved-design column instead of the batch column). We handle
        # this in remove_batch_effect by zeroing the entire coefficient row
        # whenever any NaN appears, matching R's intent for rank-deficient
        # rows regardless of which column the QR pivots out.
        import warnings as _warnings
        E = load_r_csv_no_index("rbe_rank_deficient_E_input").values
        expected = load_r_csv_no_index("rbe_rank_deficient").values
        batch = ["B1"] * 4 + ["B2"] * 4
        group = ["A"] * 4 + ["B"] * 4
        design = np.column_stack([
            np.ones(8),
            np.array([g == "B" for g in group], dtype=float),
        ])
        with _warnings.catch_warnings():
            _warnings.filterwarnings("ignore",
                                      message="Coefficients not estimable")
            got = remove_batch_effect(E, batch=batch, design=design)
        assert compare_arrays(expected, got, rtol=1e-10)["match"]
